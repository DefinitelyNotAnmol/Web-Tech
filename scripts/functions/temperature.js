// Celsius to Fahrenheit conversion
function cToF(c) {
	return (c * 9/5) + 32;
}	

// Celsius to kelvin conversion
function cToK(c) {
	return c + 273.15;
}	

// fahrenheit to celsius conversion
function fToC(f,c) {
	return c = (f - 32) * 5 / 9;
}	

