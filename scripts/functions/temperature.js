// Celsius to Fahrenheit conversion
function CtoF(c) {
	return (c * 9/5) + 32;
}	

// Celsius to kelvin conversion
function CtoK(c) {
	return c + 273.15;
}	

// fahrenheit to celsius conversion
function FtoC(f,c) {
	return c = (f - 32) * 5 / 9;
}	

// fahrenheit to kelvin conversion
function FtoK(f) {
	return 273.5 + ((f - 32.0) * (5.0/9.0)); 
}	

// kelvin to celcius conversion
function KtoC(k) {
	return k-273.15;
}	

