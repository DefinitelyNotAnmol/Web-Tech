// functions to convert mililitre into other units
function MlToLtr(ml) {
	return ml / 1000;
}

function MlToCubicM(ml) {
	return ml / 1e+6 ;
}

function MlToCubicFt(ml) {
	return ml / 28316.847;
}

function MlToCubicInch(ml) {
	return ml / 16.387;
}

function MlToPint(ml) {
	return ml / 568.261;
}

function MlToQuart(ml) {
	return ml / 1136.522;
}

function MlToGallon(ml) {
	return ml / 4546.09;
}

