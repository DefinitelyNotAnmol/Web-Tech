// functions to convert cm into something

function cmToInch(cm) {
	return cm * 0.3937;
}

function cmToM(cm) {
	return cm / 100;
}	

function cmToMm(cm) {
	return cm * 10;
}

function cmToKm(cm) {
	return cm / 100000;
}

function cmToFeet(cm) {
	return cm / 30.48;
}

function cmToYards(cm) {
	return cm / 91.44;
}

function cmToMiles(cm) {
	return cm / 160934.4;
}

function cmToNauticalMiles(cm) {
	return cm / 185200;
}

